class TokyoDriftEnv:
    def __init__(self, course_map):
        self.course_map = course_map.copy()
        self.height = course_map.shape[0]
        self.width = course_map.shape[1]
        self.velocity = np.array([0, 0])
        states=[]
        self.state_map = {}
        for i in range(len(course_map)):
            for j in range(len(course_map[1])):
                for vu in range(5):
                    for vr in range(5):
                        states.append((i,j,vu,vr))
                        self.state_map[(i,j,vu,vr)] = len(states)-1
        self.states = states
        self.reset()


    @property
    def int_state(self):
        return self.state_map[tuple([*self.state, *self.velocity])]

    @property
    def nS(self):
        return len(self.states)
    
    @property
    def nA(self):
        return 5

    def _is_terminal(self, state):
        return self.course_map[state[0], state[1]] == 3
    
    def _is_wall(self, state):
        return self.course_map[state[0], state[1]] == 1
    
    def _is_start(self, state):
        return self.course_map[state[0], state[1]] == 2
    
    def _is_drift(self, state):
        return self.course_map[state[0], state[1]] == 8
    
    def _random_start(self):
        starts = np.argwhere(self.course_map == 2)
        return starts[np.random.choice(starts.shape[0])]
    
    def reset(self):
        self.state = self._random_start()
        self.velocity = np.array([0, 0])
        return self.int_state
    
    def step(self, action):

        if self._is_terminal(self.state):
            return self.int_state, 0, True, {}

        if action == 0:
            self.velocity += np.array([0, 0])
        elif action == 1:
            self.velocity += np.array([0, 1])
        elif action == 2:
            self.velocity += np.array([0, -1])
        elif action == 3:
            self.velocity += np.array([1, 0])
        elif action == 4:
            self.velocity += np.array([-1, 0])
        else:
            raise ValueError('Invalid action')
        


        self.velocity = np.clip(self.velocity, 0, 4)
        self.state[0] = self.state[0] - self.velocity[0]
        self.state[1] = self.state[1] + self.velocity[1]
        xclipped = np.clip(self.state[0], 0, self.course_map.shape[0] - 1)
        yclipped = np.clip(self.state[1], 0, self.course_map.shape[1] - 1)
        self.state = np.array([xclipped, yclipped])

        if self._is_terminal(self.state):
            reward = 0
            done = True

        elif self._is_wall(self.state):
            reward = -5
            done = False
            self.reset()
        else:
            reward = -1
            done = False
        if self.velocity[0] == 0 and self.velocity[1] == 0:
            reward -= 20

        if self._is_drift(self.state):
            reward += 20
        
        return self.int_state, reward, done, {}
    
    def reshape_map(self, state_listlike):
        return np.reshape(state_listlike, (self.height, self.width, 5, 5))
    
    def render(self):
        course_map = self.course_map.copy()
        course_map[self.state[0], self.state[1]] = 4
        return pd.DataFrame(course_map)
    
